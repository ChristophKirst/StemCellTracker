function setPath()
%
% path = setPath()
%
% description:
%     sets all necessary paths
%

includepaths = {'/Classes', ...
        '/Filtering', ...
        '/Segmentation', ...
        '/Tracking',...
        '/Stitching', ...
        '/Interface',...
        '/Interface/ImageFormats',...
        '/Interface/ImageJ',...
        '/Interface/Imaris',...
        '/Interface/MetaMorph',...
        '/Interface/Movies',...
        '/Interface/Python',...
        '/Interface/Ilastik',...
        '/Utils',...
        '/Utils/ImTools',...
        '/Scripts',...
        ...
        '/Interface/User/BIRC',...
        '/Test'};
 
basepath = fileparts(mfilename('fullpath'));

for p = 1:length(includepaths)
   includepaths{p} = fullfile(basepath, includepaths{p});
end

addpath(includepaths{:});
     
          
% compability to matlab previous versions
v = version('-release');
if length(v) >= 4
   v = v(1:4);
   if strcmp(v, '2012')
      addpath(fullfile(basepath, '/Utils/External/Matlab2012'));
   end
end

      
end

